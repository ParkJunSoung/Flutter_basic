void main() {   //()와{ 사이 스페이스바 쳐주기
  print('Hello world');    //다트에선 탭키 두칸띄어쓰기 룰
  print(5 / 2);   // 2.5
  print(5 % 2);   // 수식사이에는 스페이스바 눌러주기
  print('5 % 2 = ${5 % 2}');

  //int x;    //Integer 변수 x의 선언
  //x= 10;    //변수 x에 10을 할당하다.
  int x = 10; //선언과 동시에 대입 (초기화)  위코드는 이제 사용x
  x = 20; //지금은 실행이 될텐데, 나중가면안될것 후반부 설명예정
  print('저는요, ${x} 살인데요.');   // 문자열 + x + 살인데요 출력
  int z = 10;
  int y = 20;print(z * y);

}

